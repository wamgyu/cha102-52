CREATE TABLE EMP3 (
	EMPNO     INT AUTO_INCREMENT NOT NULL,
	ENAME     VARCHAR(10),
	JOB       VARCHAR(9),
	HIREDATE  DATE,
	SAL       DECIMAL(7,2),
	COMM      DECIMAL(7,2),
	upFiles   LONGBLOB,
	DEPTNO    INT NOT NULL,    
	CONSTRAINT EMP3_DEPTNO_FK FOREIGN KEY (DEPTNO) REFERENCES DEPT3 (DEPTNO),
	CONSTRAINT EMP3_EMPNO_PK PRIMARY KEY (EMPNO)
) AUTO_INCREMENT = 7001;

