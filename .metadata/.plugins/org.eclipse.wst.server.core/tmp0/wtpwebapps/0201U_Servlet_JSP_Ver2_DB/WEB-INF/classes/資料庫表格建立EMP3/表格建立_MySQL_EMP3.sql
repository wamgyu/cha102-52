	DEPTNO	INT AUTO_INCREMENT NOT NULL,
	DNAME	  VARCHAR(14),
	LOC		  VARCHAR(13),
	CONSTRAINT DEPT3_PRIMARY_KEY PRIMARY KEY (DEPTNO)
);

